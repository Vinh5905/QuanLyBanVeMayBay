IF OBJECT_ID(N'dbo.HANGVE', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.HANGVE (
        MaHangVe         INT IDENTITY(1,1) NOT NULL CONSTRAINT PK_HANGVE PRIMARY KEY,
        TenHangVe        NVARCHAR(50) NOT NULL CONSTRAINT UQ_HANGVE_TenHangVe UNIQUE,
        HeSoGia          DECIMAL(8,2) NOT NULL CONSTRAINT DF_HANGVE_HeSoGia DEFAULT 1,
