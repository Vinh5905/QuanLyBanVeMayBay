        MaTaiKhoan     INT NULL,
        TenHanhDong    VARCHAR(100) NOT NULL,
        TenBang        VARCHAR(100) NULL,
        MaBanGhi       VARCHAR(50) NULL,
        DuLieuCu       NVARCHAR(MAX) NULL,
        DuLieuMoi      NVARCHAR(MAX) NULL,
        IpAddress      VARCHAR(50) NULL,
        UserAgent      VARCHAR(500) NULL,
        ThoiGian       DATETIME2(0) NOT NULL CONSTRAINT DF_AUDIT_LOG_ThoiGian DEFAULT SYSUTCDATETIME(),
        CONSTRAINT FK_AUDIT_LOG_TAIKHOAN FOREIGN KEY (MaTaiKhoan) REFERENCES dbo.TAIKHOAN(MaTaiKhoan)
    );
END;
GO

IF OBJECT_ID(N'dbo.APP_CONFIG', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.APP_CONFIG (
        ConfigKey      VARCHAR(100) NOT NULL CONSTRAINT PK_APP_CONFIG PRIMARY KEY,
        ConfigValue    NVARCHAR(500) NOT NULL,
